# A DEFINITE LOOP with STRINGS

friends = ['John', 'Mike', 'Sally']
# friends here is the name of the variable

for friend in friends:
# friend is the iteration variable, this could be i it makes no difference what we call the iteration variable
# Python does NOT understand plural
# [a list, a sequence of strings]
  print('Happy New Year:', friend)
print('Done!')

